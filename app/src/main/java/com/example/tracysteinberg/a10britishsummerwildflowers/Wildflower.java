public class Wildflower {


        private int ranking;
        private String name;

        public Wildflower(Integer ranking, String name) {
            this.ranking = ranking;
            this.name = name;

        }

        public Integer getRanking() {
            return ranking;
        }

        public String getName() {
            return name;
        }


}
